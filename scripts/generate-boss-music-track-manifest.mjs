import { readdirSync, writeFileSync } from 'node:fs';
import { join, relative, sep } from 'node:path';

const musicRoot = join('ASSETS', 'music', 'BossMusic');
const outputPath = join('src', 'audio', 'generated-boss-music-tracks.ts');
const supportedExtensions = new Set(['.mp3', '.ogg', '.wav']);
const tracksByBpm = {};

function scan(directory) {
  for (const entry of readdirSync(directory, { withFileTypes: true })) {
    const fullPath = join(directory, entry.name);
    if (entry.isDirectory()) { scan(fullPath); continue; }
    const extension = entry.name.slice(entry.name.lastIndexOf('.')).toLowerCase();
    if (!supportedExtensions.has(extension)) continue;
    const relativeParts = relative(musicRoot, fullPath).split(sep);
    const bpmMatch = /^(\d+)BPM$/i.exec(relativeParts[0] ?? '');
    if (!bpmMatch) continue;
    const bpm = Number(bpmMatch[1]);
    (tracksByBpm[bpm] ??= []).push(relativeParts.join('/'));
  }
}

scan(musicRoot);
for (const tracks of Object.values(tracksByBpm)) tracks.sort();
writeFileSync(outputPath, [
  '/** Generated by scripts/generate-boss-music-track-manifest.mjs. Do not edit manually. */',
  'export const BOSS_MUSIC_TRACKS_BY_BPM: Readonly<Record<number, readonly string[]>> = ' + JSON.stringify(tracksByBpm, null, 2) + ';',
  '',
].join('\n'));
