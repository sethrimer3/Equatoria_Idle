import { describe, expect, it } from 'vitest';
import { parseBossMidi } from '../boss-midi-parser';

function demoMidi(): ArrayBuffer {
  return new Uint8Array([
    0x4d,0x54,0x68,0x64,0x00,0x00,0x00,0x06,0x00,0x01,0x00,0x02,0x01,0xe0,
    0x4d,0x54,0x72,0x6b,0x00,0x00,0x00,0x0b,0x00,0xff,0x51,0x03,0x07,0xa1,0x20,0x00,0xff,0x2f,0x00,
    0x4d,0x54,0x72,0x6b,0x00,0x00,0x00,0x2f,
    0x00,0xff,0x03,0x06,0x51,0x75,0x61,0x72,0x74,0x7a,
    0x00,0xc0,0x50,
    0x00,0x90,0x24,0x50,
    0x83,0x60,0x80,0x24,0x40,
    0x00,0x91,0x3c,0x70,
    0x83,0x60,0x81,0x3c,0x40,
    0x00,0x92,0x48,0x78,
    0x87,0x40,0x82,0x48,0x40,
    0x00,0xff,0x2f,0x00,
  ]).buffer;
}

describe('parseBossMidi', () => {
  it('normalizes MIDI notes with ms, beats, channel, velocity, track, and instrument', () => {
    const notes = parseBossMidi(demoMidi());
    expect(notes).toHaveLength(3);
    expect(notes[0]).toMatchObject({
      timeMs: 0,
      durationMs: 500,
      beat: 0,
      durationBeats: 1,
      note: 36,
      velocity: 80,
      channel: 0,
      trackName: 'Quartz',
      instrument: 80,
    });
    expect(notes[1].timeMs).toBe(500);
    expect(notes[1].channel).toBe(1);
    expect(notes[2].timeMs).toBe(1000);
    expect(notes[2].durationMs).toBe(1000);
  });

  it('throws on unsupported data instead of returning misleading events', () => {
    expect(() => parseBossMidi(new Uint8Array([1, 2, 3]).buffer)).toThrow(/MThd/);
  });
});
